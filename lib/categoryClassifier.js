function classifyCategory({ title = "", sellerTitle = "", categoryTitle = "" } = {}) {
  for (const [code, re] of CATEGORY_TITLE_RULES) {
    if (re.test(categoryTitle)) return code;
  }

  const haystack = `${title} ${sellerTitle}`.toLowerCase();
  for (const [code, keywords] of Object.entries(TITLE_KEYWORD_RULES)) {
    for (const kw of keywords) {
      if (haystack.includes(kw.toLowerCase())) return code;
    }
  }

  return "ETC";
}

module.exports = { classifyCategory };
